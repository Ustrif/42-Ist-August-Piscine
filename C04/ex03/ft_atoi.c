int	ft_atoi(char	*str)
{
	int	say;
	int	l;

	say = 0;
	l = 1;
	while ((9 <= *str && *str <= 13) || *str == 32)
		str++;
	while (*str == '-' || *str == '+')
	{
		if (*str == '-')
			l = l * -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		say = (say * 10) + *str - '0';
		str++;
	}
	return (l * say);
}
